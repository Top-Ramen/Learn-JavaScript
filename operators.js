// Arithmetic Operators
var a = 25
var b = 10
console.log("Addition: "+(a+b))
console.log("Subtraction: "+(a-b))
console.log("Multiplication: "+(a*b))
console.log("Division: "+(a/b))
console.log("Modulo: "+(a%b))

// Increment Operator
var x = 10;
// Next line increment for the post incremental
console.log(x++)
console.log(x)
// Same line increment for the pre incremental
console.log(++x)
var y = 10
// Next line decrement for the post decremental
console.log(y--)
console.log(y)
// Same line decrement for the pre decremental
console.log(--y)
